import { PlanTemplate, PlanTemplateDay } from "@/types/plan";

const block = (title: string, items: string[]) => ({ title, items });

function copyBlock(source?: { title: string; items: string[] }) {
  return source ? { title: source.title, items: [...source.items] } : undefined;
}

function cloneTemplateDay(day: PlanTemplateDay, overrides: Partial<PlanTemplateDay> = {}): PlanTemplateDay {
  const merged = { ...day, ...overrides };

  return {
    ...merged,
    contentIds: [...(overrides.contentIds ?? day.contentIds)],
    drills: [...(overrides.drills ?? day.drills)],
    drillsEn: [...(overrides.drillsEn ?? day.drillsEn)],
    warmupBlock: copyBlock(overrides.warmupBlock ?? day.warmupBlock),
    warmupBlockEn: copyBlock(overrides.warmupBlockEn ?? day.warmupBlockEn),
    mainBlock: copyBlock(overrides.mainBlock ?? day.mainBlock),
    mainBlockEn: copyBlock(overrides.mainBlockEn ?? day.mainBlockEn),
    pressureBlock: copyBlock(overrides.pressureBlock ?? day.pressureBlock),
    pressureBlockEn: copyBlock(overrides.pressureBlockEn ?? day.pressureBlockEn),
    successCriteria: [...(overrides.successCriteria ?? day.successCriteria ?? [])],
    successCriteriaEn: [...(overrides.successCriteriaEn ?? day.successCriteriaEn ?? [])]
  };
}

function cloneTemplateDays(
  days: PlanTemplateDay[],
  overrides: Record<number, Partial<PlanTemplateDay>> = {}
): PlanTemplateDay[] {
  return days.map((day) => cloneTemplateDay(day, overrides[day.day]));
}

